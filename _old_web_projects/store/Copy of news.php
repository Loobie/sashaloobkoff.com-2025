		    <p><b>Last Days of April</b></p> 
            <p><a href="bands.php?id=6"><img src="art/ldoa_live.jpg" alt="::LDOA::" hspace="6px" border="0"></a></p>
            <p>We are happy to announce the addition of <a href=&quot;bands.php?id=6>Last 
              Days of April</a> to our roster. Check out the song on the mp3 player 
              below and buy a CD. Nobody will be disappointed... I guarantee it.</p>
            <p>Ya know, in the last year I've had so many new favorite bands... and LDOA were 
              one of the first. </p>
            <p>Weird for me because I'm a jaded old creep who's usually way too 
              closed minded about music to give a fuck. I suppose discovering 
              these bands is what prompted me to build this site in the first 
              place. </p>
            <p>My list of new favorite bands (in chronological order): </p>
			<ol>
              <li><b>Speed not Steel</b>: What do you do when 2 guys from your 
                band team up and form a group that is at least as good or better 
                than what your doing together? Get jealous as hell... that's what 
                I did. But what good are you if you can't support your friends... 
                right? SNS is comprised of two of the most incredible, heart felt 
                songwriters you'll ever find. Awesome. </li>
              <br>
              <br>
              <li><b>Theory of Ruin</b>: I'm the hugest Fudge Tunnel fan there 
                is. Best heavy guitar sound ever produced with the exception of 
                maybe Slayer and The Refused (the only other 2 bands that match 
                FT's ferocity). So when Alex started this band and told me that 
                he was gonna tone down the guitar and let the vocals, bass and 
                drums take center stage, I was bummed. I thought it was lame. 
                Then a couple years later they released their 2nd EP and played 
                a couple of great, great shows in L.A. and I got over it. In a 
                big way. Why they aren't the biggest heavy band around is beyond 
                me.</li>
              <br>
              <br>
              <li><b>Communique</b>: Cutlass was once supposed to play a show 
                with these guys in Oakland and I thought &quot;who the fuck are 
                these dorks? I mean that must be the stupidest band name ever 
                and, jeezus, they're on Lookout!. Bleeech.&quot; Boy was I wrong. 
                A year later my friend Eric played their first ep for me because 
                he played drums on it and I immediately regretted that the aforemention 
                show never materialized.</li>
              <br>
              <br>
              <li><b>Solea</b>: Okey, so I know I'm not the most impartial source 
                on this one because it's my twin brother's band BUT believe me, 
                if you toured Japan with them (like I did last March) and saw 
                how they killed every night you'd love 'em too. </li>
              <br>
              <br>
              <li><a href="http://www.hardwoodrecords.com/" target="_newWindow"><img src="art/hayden.jpg" alt="::Hayden::" width="165" height="165" hspace="6px" border="0" align="right" style="border:#FFFFFF 1px solid; margin-left: 16px; margin-bottom: 12px;"></a><b>Hayden</b>: 
                Admittedly an acquired taste. Took me about 6 years before I admitted 
                to myself that I liked this Canadian dude. I mean he screams to 
                folk music... how weird is that? Now I love it.</li>
              <br>
              <br>
              <li><b>Last Days of April</b>: Have I ever told you that I broke 
                up with my long time girlfriend last year? That was a real nightmare. 
                Then (predictibly) my rebound girl threw me for one as well (although 
                I could give 2 shits about that now). When I moved into my new 
                house in L.A. I fell asleep to LDOA's most recent record (&quot;If 
                You Lose It&quot;) every night for about 3 straight months. Probably 
                because every song is about breaking up. I suppose I enjoyed feeling 
                sorry for myself. Faig. Eventually I had to quit because I realized 
                it was depressing the hell out of me. Glad I'm over that shit 
                now (at least partially). </li>
              I like listening to that record.<br>
              <br>
              <li><b>Pedro the Lion</b>: &quot;Control&quot; and &quot;Winners 
                Never Quit&quot; are two of the best CDs ever recorded. Period. 
                Lyrically they are audio novels where each song is a chapter. 
                Amazing. Funny because this is another band Cutlass was once billed 
                with (Jets to Brazil headlined and Sergie filled in on bass for 
                us). I wasn't too impressed. I thought they were lame-o emo dudes. 
                Again I was wrong.</li>
              <br>
              <br>
              <li><b>Spoon</b>: This is my new favorite though the only CD of 
                theirs I have is &quot;Girls Can Tell.&quot; My new girlfriend 
                was playing it at &quot;11&quot; the first time I visited her 
                so I suppose I'll always have a soft spot for these guys. I was 
                so stoked! (But then again I'd probably be just as excited if 
                she were listening to Menudo or some other crappy band.) Their 
                sound is so minimal and spacious. I fucking love it. Exactly the 
                type of music I've wanted to play (and for the most part failed 
                to) for the last 10 years of being in bands. Funny thing is that 
                they once opened for Knapsack in SF and I thought they kinda sucked. 
                Apparently so did the Knapsack and Archers of Loaf fans in attendance. 
                Maybe their older stuff isn't as good. </li>
            </ol>
